    public void totalTable() {

        int numrow = jtVenda.getRowCount();

        double total = 0;

        for (int i = 0; i < numrow; i++) {

            double valor = Double.parseDouble(jtVenda.getValueAt(i, 6).toString());
            total += valor;

        }

        jlTotal.setText("Total:   " + nf.format(total));

        jlRowCount.setText(String.valueOf(numrow) + " Registros");

    }

