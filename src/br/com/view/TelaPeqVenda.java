    public void opemTelaVenda() throws IOException, ParseException {

        File file = new File("./src/properties/query.properties");
        
        if(file.exists()){
        
        Propries get = new Propries();

        dcDataInicio.setDate(dt.parse(get.getProp("data.inicio", file)));
        dcDataFim.setDate(dt.parse(get.getProp("data.fim", file)));
        txtCli.setText(get.getProp("cod.cliente", file));
        txtNvenda.setText(get.getProp("nun.venda", file));
        cbxLoja.setSelectedItem(get.getProp("num.loja", file));
        cbxTipoVenda.setSelectedItem(get.getProp("tipo.venda", file));
        }
            

    }

